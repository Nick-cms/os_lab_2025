#!/bin/bash

# Проверяем, что аргументы переданы
if [ $# -eq 0 ]; then
    echo "Ошибка: нет входных аргументов"
    exit 1
fi

# Инициализируем переменные
sum=0
count=$#

# Проходим по всем аргументам
for arg in "$@"; do
    # Суммируем числа
    sum=$(($sum + $arg))
done

# Вычисляем среднее
average=$(($sum / $count))

# Выводим результат
echo "Количество чисел: $count"
echo "Среднее арифметическое: $average"
